Kötüye kullanım bildirme

Aşağıda bulunan politikaların ihlal edildiğini düşünüyorsanız kötüye kullanımı bildirin.

Telif hakkı ihlalini veya diğer yasal sorunları bildirmek için lütfen bu aracı kullanın. Söz konusu araç, 
geçerli yasalar uyarınca Açıklandı.com'un hizmetlerinden kaldırılması gerektiğini düşündüğünüz içeriği bildirme sürecinde 
size yol gösterir.



Program politikaları

Spam, kötü amaçlı yazılım ve kimlik avı
Lütfen spam'i ve kötü amaçlı yazılımları ürünlerimizden uzak tutun. 
Spam; tanıtım amaçlı veya ticari istenmeyen içeriği ve istenmeyen ya da toplu talepleri kapsar, 
ancak bunlarla sınırlı değildir. Ürünlerimizi kimlik avı için kullanmayın. Şifreler, 
mali bilgiler ve vatandaşlık numaraları dahil, ancak bunlarla sınırlı olmamak üzere, 
hassas verileri istemeyin veya toplamayın. Kötü amaçlı yazılımların, virüslerin, zararlı 
kodların veya Google ya da diğer kuruluşların ağ, sunucu veya başka altyapılarının 
işleyişine zarar verebilecek ya da işlemesini engelleyebilecek öğelerin iletimine de izin vermiyoruz.


Şiddet

Ne düşündüğünüzü açıkça söyleyin, ancak başkalarını tehdit etmeyin. Diğer bir deyişle, başka bir kişinin fikirlerine katılmadığınızı açıkça belirtebilirsiniz, ancak söz konusu kişiyi ciddi fiziksel yaralanma veya ölümüne sebep olmakla tehdit etmeyin ya da başkalarına fiziksel zarar vermek için destek toplamaya çalışmayın. Çoğu durumda, tartışma veya anlaşmazlıkların bizim tarafımızdan değil, ilgili taraflarca en iyi şekilde çözülebildiğini görüyoruz. Ancak, ciddi ve olması muhtemel fiziksel bir yaralama veya ölüm tehdidi söz konusuysa içerikle ilgili işlem yapabiliriz.
Temel amacı insanları sarsmak, üzmek veya asılsız bilgiler vermek olan şiddet barındıran ya da korkutucu içeriklerin yayınlanması uygun değildir. Haber veya belgesel bağlamında ya da bilimsel veya sanatsal bağlamda sansürsüz içerik yayınlıyorsanız insanların neler olduğunu anlamalarına yardımcı olacak kadar bilgi sağlayın. Bazı durumlarda içerik o kadar çok şiddet barındırıyor veya sarsıcı olabilir ki platformlarımızda hiçbir şekilde kalmasına izin verilmez. Son olarak, başkalarını belirli şiddet eylemlerinde bulunmaya teşvik etmeyin.

Nefret söylemi

Postlarımız kişilerin özgürce içerik paylaşabileceği platformdur, ancak nefret söylemine izin vermeyiz. Nefret söylemi; ırk, etnik köken, dini inanç, engellilik, yaş, milliyet, gazilik durumu, cinsel yönelim, cinsiyet, cinsel kimlik veya sistemli ayrımcılık ya da marjinalleştirmeyle ilişkili diğer özelliklere göre bireylere ya da gruplara karşı şiddeti teşvik eden veya mazur gösteren ya da nefret uyandırmayı amaçlayan içeriklerdir.


Terör amaçlı içerik

Terör örgütlerinin, Drive'ı adam toplamak da dahil herhangi bir amaçla kullanmasına izin verilmez. Ayrıca Drive, terörizmle alakalı içerikleri (terörist faaliyetleri destekleyen, şiddeti teşvik eden veya terör saldırılarını öven içerikler gibi) kesin bir şekilde yasaklar. 

Terörizm hakkında eğitici, belge niteliği taşıyacak, bilimsel veya sanatsal bir amaçla içerik yayınlıyorsanız, görüntüleyenlerin bağlamı doğru bir şekilde anlamaları için yeterli bilgi sağlamaya özen gösterin.


Yanıltıcı içerik

Kullanıcıları aldatan, yanıltan veya kafa karışıklığı yaratan içerikler dağıtmayın. Buna aşağıdakiler dahildir:

Sivil ve demokratik süreçlerle ilgili yanıltıcı içerik: Bariz şekilde yanlış olan ve sivil ya da demokratik süreçlere katılımı veya güveni önemli ölçüde sarsabilecek içerik. Buna, resmi devlet kayıtlarıyla çakışan halk oylaması prosedürleri, yaş/doğum yerine dayalı siyasi aday uygunluğu veya nüfus sayımına katılım ile ilgili bilgiler dahildir. Ayrıca, siyasi bir kişinin veya devlet görevlisinin hayatını kaybettiği, kazaya karıştığı veya ani bir ciddi rahatsızlık geçirdiği yönünde yanlış iddiaları da kapsar.

Zararlı sağlık uygulamalarıyla ilgili yanıltıcı içerik: Bireylere ciddi fiziksel veya duygusal zarar verebilecek ya da kamu sağlığının zararına olan uygulamalara katılmayı özendiren veya teşvik eden sağlıkla ilgili ya da tıbbi içerik.

Manipüle edilmiş medya: Kullanıcıları yanıltacak ve korkunç bir zarar riski oluşturabilecek şekilde teknik olarak manipüle edilmiş veya değiştirilmiş medya.

Yanıltıcı içeriğe eğitici, belge niteliği taşıyacak, bilimsel veya sanatsal bağlamlarda izin verilebilir, ancak kullanıcıların bu bağlamı doğru bir şekilde anlamaları için yeterli bilgi sağlamaya özen gösterin. Bazı durumlarda içerik miktarı ne olursa olsun içerik, platformlarımızda kalmaz.



Kişisel ve gizli bilgiler

Kişisel veya gizli bilgiler paylaşmayın. Kredi kartı numaraları, vatandaşlık numaraları, sürücü belgesi ve diğer lisanslara ait numaralar gibi özel ve gizli bilgilerin veya kamuya açık olmayan tüm diğer bilgilerin yetkisiz yayınlanmasına izin vermiyoruz. Çoğu durumda, internette veya kamuya açık kayıtlarda zaten sunulan bilgilerin politikalarımız kapsamında özel veya gizli olarak kabul edilmediğini unutmayın.

Ayrıca, yasal temsilcilerinden gerekli izni almadan küçüklerin resimlerini ya da videolarını yayınlamayın veya dağıtmayın.



