<b>3. Tarafların Hak ve Yükümlülükleri</b>

<p>
3.1. Üye, www.aciklandi.com internet sitesine üye olurken verdiği kişisel ve diğer sair bilgilerin kanunlar önünde doğru olduğunu, Açıklandı'nin bu bilgilerin gerçeğe aykırılığı nedeniyle uğrayacağı tüm zararları aynen ve derhal tazmin edeceğini beyan ve taahhüt eder.
</p>
<p>
