Bu sebeple, sitemiz uyar ve kaldır prensibini benimsemiştir. 
Telif hakkına konu olan eserlerin yasal olmayan bir biçimde paylaşıldığını ve yasal haklarının çiğnendiğini 
düşünen hak sahipleri veya meslek birlikleri, “contact@aciklandi.com” mail adresinden bize ulaşabilirler. 
Buraya ulaşan talep ve şikayetler hukuksal olarak incelenecek, şikayet yerinde görüldüğü takdirde,
